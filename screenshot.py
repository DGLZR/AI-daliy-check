import cv2
import numpy as np
import mss
import ollama
import base64


def capture_screen():
    """截图函数：检测显示器分辨率并全屏截图，返回OpenCV格式图像"""
    with mss.mss() as sct:
        monitor = sct.monitors[1]      # 1为主显示器，0为全部显示器
        screenshot = sct.grab(monitor)  # 全屏截图
        img = np.array(screenshot)      # 转换为numpy数组
        img = cv2.cvtColor(img, cv2.COLOR_BGRA2BGR)  # BGRA转BGR（OpenCV格式）
        return img


def ollama_recognize():
    """调用ollama识别截图内容"""
    # 截图
    img = capture_screen()
    
    
    # 转换为base64编码（ollama需要base64格式的图像）
    _, buffer = cv2.imencode('.png', img)
    img_base64 = base64.b64encode(buffer).decode('utf-8')
    
    # 提示词（用户可自行修改）
    prompt = """根据当前电脑屏幕截图，记录此刻正在进行的工作活动。

核心目标：
1. 尽可能记录有日报价值的工作内容。
2. 保留工作主题、任务名称、项目方向、需求内容、技术问题、交付物、进展、待办。
3. 保护员工隐私：不记录私人聊天细节，不记录联系人身份，不记录账号、密钥、完整链接等敏感信息。
4. 不要因为隐私保护而把所有内容都写成空泛描述。只需要脱敏"身份信息"和"敏感字段"，工作任务本身要尽量保留。

你应该记录什么：
1. 当前正在做的具体工作任务。
2. 当前正在阅读、编辑、核对、调试、整理、沟通的内容主题。
3. 当前任务属于哪个工作方向，例如：产品设计、代码开发、运营分析、数据复盘、客户支持、项目管理、日报整理、资料研究等。
4. 能用于日报的具体进展。
5. 如果是在聊天工具里处理工作，要提取"工作事项"和"任务含义"，但不要复述聊天原文。

你不应该记录什么：
1. 不要描述桌面壁纸、系统状态栏、时间、电量、天气、Dock、任务栏等无关信息。
2. 不要输出联系人昵称、群名、备注名、头像文字、账号名。
3. 不要逐字复述聊天消息。
4. 不要输出私人聊天细节。
5. 不要输出手机号、邮箱、身份证、银行卡、地址、验证码、密码、Token、API Key、Cookie。
6. 不要输出客户个人身份、员工个人身份、具体薪酬绩效、敏感财务明细。

关于聊天和沟通界面的处理：
如果截图中出现微信、飞书、钉钉、Slack、Telegram、邮件、私信、群聊、文件传输助手、评论区等沟通界面：

允许输出：
1. 沟通的工作主题。
2. 沟通涉及的任务方向。
3. 可识别出的工作需求、待办、结论、进展。
4. 文件或链接的大致用途。
5. 员工正在处理这些沟通信息的行为。

禁止输出：
1. 联系人是谁。
2. 群名是什么。
3. 对方原话是什么。
4. 聊天消息逐字内容。
5. 完整链接、账号、手机号、邮箱、订单号、密钥等。

示例：
如果截图里聊天内容是：
"做一个 ios 的原生 app 原型"
可以输出：
"正在整理或沟通 iOS 原生 App 原型设计相关任务。"

如果截图里聊天内容是：
"做海龟策略回测"
可以输出：
"正在处理量化交易策略回测相关任务。"

如果截图里聊天内容是：
"@某人: 某某仓库/web-design"
可以输出：
"正在通过沟通工具同步一个 Web 设计或前端页面相关的代码/设计任务。"
不要输出具体人员、账号、仓库完整名称。

如果截图里聊天内容是私人闲聊、家庭、朋友、情感、生活琐事：
只输出：
"当前包含私人沟通内容，具体内容已脱敏，不纳入日报。"

以下内容必须脱敏：
1. 具体人员身份。
2. 具体群名、客户名、公司内部名称。
3. 完整链接、账号、仓库完整路径。
4. 密钥、Token、验证码、密码。
5. 私人聊天原文。
6. 敏感业务数据明细。

输出要求：
1. 内容要对日报有用。
2. 允许保留工作任务细节。
3. 不要泄露个人身份和敏感字段。
4. 不要逐字复述聊天内容。
5. 不要输出无关桌面环境。
6. 不要编造截图中不存在的内容。
7. 如果隐私和工作记录冲突，优先保留"脱敏后的工作事项"，而不是完全隐藏。

请严格按照以上规则输出。"""
    
    # 创建ollama客户端，指定远程IP地址
    client = ollama.Client(host='http://192.168.31.23:11434')  # 修改为实际的ollama服务器IP
    
    # 调用ollama进行图像识别
    response = client.chat(
        model='minicpm-v4.6',  # 模型名称（根据本地安装的模型修改）
        messages=[{
            'role': 'user',
            'content': prompt,
            'images': [img_base64]  # 传入base64编码的图像
        }]
    )
    
    # 返回识别结果文本
    return response['message']['content']

output=ollama_recognize()
print(output) 